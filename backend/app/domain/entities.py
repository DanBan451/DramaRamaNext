"""
Domain Entities - Core business objects
"""
from pydantic import BaseModel
from typing import Literal, Optional, List
from datetime import datetime
from enum import Enum
import json

class Element(str, Enum):
    EARTH = "earth"
    FIRE = "fire"
    AIR = "air"
    WATER = "water"
    CHANGE = "change"

class SubElement(str, Enum):
    ONE = "1.0"
    TWO = "2.0"
    THREE = "3.0"
    TRANSFORM = "transform"

class SessionStatus(str, Enum):
    IN_PROGRESS = "in_progress"
    COMPLETED = "completed"
    ABANDONED = "abandoned"

class User(BaseModel):
    id: str
    clerk_id: str
    email: Optional[str] = None
    created_at: Optional[datetime] = None
    updated_at: Optional[datetime] = None
    # Archetype (generated by LLM after first completed session)
    archetype_name: Optional[str] = None  # e.g., "The Groundkeeper"
    archetype_description: Optional[str] = None  # 1 sentence description
    avatar_image_url: Optional[str] = None  # DALL-E generated avatar based on elements

class Session(BaseModel):
    id: str
    user_id: str
    puzzle_id: Optional[str] = None
    problem_description: Optional[str] = None
    thinker_description: Optional[str] = None  # Generated on first session completion
    started_at: datetime
    ended_at: Optional[datetime] = None
    status: SessionStatus = SessionStatus.IN_PROGRESS
    prompts_completed: int = 0
    created_at: Optional[datetime] = None
    # Cube visual properties (generated by LLM based on problem description)
    cube_primary_color: Optional[str] = None
    cube_secondary_color: Optional[str] = None
    cube_complexity: Optional[int] = None  # 1-5
    cube_label: Optional[str] = None  # 2-word label
    cube_image_url: Optional[str] = None  # DALL-E generated image URL
    # Unified understanding document (built/updated by LLM during session)
    understanding_document: Optional[str] = None

class Response(BaseModel):
    id: str
    session_id: str
    prompt_index: int
    element: Element
    sub_element: SubElement
    response_text: str
    word_count: int
    time_spent_seconds: int
    created_at: Optional[datetime] = None

class Hint(BaseModel):
    id: str
    session_id: str
    hint_text: str
    element_focus: Optional[Element] = None
    patterns_detected: Optional[dict] = None
    user_final_response: Optional[str] = None
    created_at: Optional[datetime] = None

class Puzzle(BaseModel):
    id: str
    title: str
    scenario: str
    constraints: List[str] = []
    example: str
    solution: str
    created_at: Optional[datetime] = None

class Component(BaseModel):
    id: str
    session_id: str
    puzzle_id: str
    user_id: str
    title: str
    key_insight: str
    input_context: str
    output_capability: str
    created_at: Optional[datetime] = None

class ElementMessage(BaseModel):
    id: str
    session_id: str
    prompt_index: int = 0  # Kept for backward compatibility, not used in new architecture
    role: str  # "user" or "assistant"
    message_text: str
    element_applied: Optional[str] = None  # Which element was invisibly applied (earth/fire/air/water/change)
    created_at: Optional[datetime] = None

class DeepUnderstanding(BaseModel):
    id: str
    session_id: str
    prompt_index: int = 0  # Kept for backward compatibility, not used in new architecture
    element: str  # Which element was invisibly applied
    insight_text: str
    created_at: Optional[datetime] = None

